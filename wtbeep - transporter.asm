playNote
	add hl,de	;11	
	ld a,h		;4

algo1	
	ds 5		;20


	sra	a
	sra	a
	sra	a

	XOR   	1
	ld	($6800),a	
	exx		;4
	
	add hl,bc	;11
	ld a,h		;4

algo2	
	ds 5		;20
	
;			inc bc		;6		;timing

	sra	a
	sra	a
	sra	a

	XOR   	1
	ld	($6800),a	
	ex de,hl	;4
	
	add hl,sp	;11
	ld a,h		;4

algo3	
	ds 5		;20
	
;	nop		;4		;timing
;	nop		;4
	
	ex de,hl	;4
	

	sra	a
	sra	a
	sra	a

	XOR   	1
	ld	($6800),a	
	
	exx		;4
	
	dec c		;4
	jp nz,playNote	;10
			;184
	
	inc iyl				;update sweep counters
	ld a,iyl
	rrca
	rrca
	ld iyh,a
	rrca
	ld ixh,a
	
	dec b
	jp nz,playNote

